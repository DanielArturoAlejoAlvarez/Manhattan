    private void btnGuardarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPActionPerformed
        // TODO add your handling code here:
        //Validations
        if (txtNombreProducto.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter name of room");
            txtNombreProducto.requestFocus();
            return;
        }
        if (textAreaDescripcionProducto.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter description of room");
            textAreaDescripcionProducto.requestFocus();
            return;
        }
        
        if (txtPrecioVenta.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter sale price of room");
            txtPrecioVenta.requestFocus();
            return;
        }

        vproducto dts = new vproducto();
        fproducto func = new fproducto();

        dts.setNombre(txtNombreProducto.getText());
        dts.setDescripcion(textAreaDescripcionProducto.getText());
        int s = cboUnidadMedida.getSelectedIndex();        
        dts.setUnidad_medida((String) cboUnidadMedida.getItemAt(s));
        dts.setPrecio_venta(Double.parseDouble(txtPrecioVenta.getText()));
        
        

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "The product was registered successfully!");
                mostrar("");
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            dts.setIdproducto(Integer.parseInt(txtIdProducto.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "The product was edited successfully!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarPActionPerformed

