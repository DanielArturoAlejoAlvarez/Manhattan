    private void btnGuardarTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarTActionPerformed
        // TODO add your handling code here:
        //Validations
        
        
        if (txtNombreTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter first name of client");
            txtNombreTrabajador.requestFocus();
            return;
        }
        
        if (txtApellidosTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter last name of client");
            txtApellidosTrabajador.requestFocus();
            return;
        }

        if (txtNumeroDocTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter number doc. of client");
            txtNumeroDocTrabajador.requestFocus();
            return;
        }
        
        if (txtDireccionTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter address of client");
            txtDireccionTrabajador.requestFocus();
            return;
        }
        
        if (txtTelefonoTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter phone of client");
            txtTelefonoTrabajador.requestFocus();
            return;
        }
        
        if (txtEmailTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter email of client");
            txtEmailTrabajador.requestFocus();
            return;
        }
        
        if (txtSueldoTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter salary of client");
            txtSueldoTrabajador.requestFocus();
            return;
        }
        
        if (txtLoginTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter login of client");
            txtLoginTrabajador.requestFocus();
            return;
        }
        if (txtPasswordTrabajador.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Please enter password of client");
            txtPasswordTrabajador.requestFocus();
            return;
        }
        
        

        vtrabajador dts = new vtrabajador();
        ftrabajador func = new ftrabajador();

        dts.setNombre(txtNombreTrabajador.getText());
        dts.setApellidos(txtApellidosTrabajador.getText());
        int s = cboTipoDocTrabajador.getSelectedIndex();
        dts.setTipo_documento((String) cboTipoDocTrabajador.getItemAt(s));
        dts.setNum_documento(txtNumeroDocTrabajador.getText());
        dts.setDireccion(txtDireccionTrabajador.getText());
        dts.setTelefono(txtTelefonoTrabajador.getText());
        dts.setEmail(txtEmailTrabajador.getText());
        dts.setSueldo(Double.parseDouble(txtSueldoTrabajador.getText()));
        int s2 = cboAccesoTrabajador.getSelectedIndex();
        dts.setAcceso((String) cboAccesoTrabajador.getItemAt(s2));
        dts.setLogin(txtLoginTrabajador.getText());
        dts.setPassword(txtPasswordTrabajador.getText());
        int s3 = cboEstadoTrabajador.getSelectedIndex();
        dts.setEstado((String) cboEstadoTrabajador.getItemAt(s3));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "The client was registered successfully!");
                mostrar("");
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtIdTrabajador.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "The client was edited successfully!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarTActionPerformed

